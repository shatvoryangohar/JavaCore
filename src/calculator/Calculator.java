package calculator;

public class Calculator {

 public   int gumarum(int a, int b) {


        return a + b;
    }


 public   int hanum(int a, int b) {
        return a - b;
    }

 public   int bajanum(int a, int b) {
        return a / b;
    }


 public   int bazmapatkum(int a, int b) {
        return a * b;
    }


}
